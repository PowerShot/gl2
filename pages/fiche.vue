    <h1 class="title">Activités actuelles</h1>

    Accompagner une personne dans la réalisation de ses activités quotidiennes (aider à la toilette, à l'habillage, au déshabillage…) et au niveau des tâches domestique (entretien du logement, du linge, courses, préparation des repas, démarches administratives…).
