    Il accompagne les personnes sur leur lieu de vie dans l'accomplissement d'activités courantes (ex. entretien de la maison, tâches administratives quotidiennes) et aide les personnes ne pouvant plus faire en totale autonomie et/ou rencontrant des difficultés passagères ou durables. </p>
